function potencia(base, exponente){
    return Math.pow(base, exponente)
}

function raiz(numero){
    return Math.sqrt(numero);
}

function absoluto(numero){
    return Math.abs(numero);
}

function numeropi(){
    return Math.PI
}




console.log("El número es : "+potencia(9,2));
console.log("La raiz cuadra es : " + raiz(4));
console.log("El número absoluto es : "+absoluto(-986));
console.log("El numero PI es : "+numeropi());